    ClsReserva Reserva = new ClsReserva();
    DecimalFormat formatter = new DecimalFormat("#0.00");
    
    private void btnDieselMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDieselMouseClicked
        int Tipo;
        double Valor;
        String Gas = "Diesel";
        
        Tipo = cmbOpcion.getSelectedIndex() + 1;
        Valor = Double.parseDouble(txtCantidad.getText());
        Reserva.setTipo(Tipo);
        Reserva.setCantidad(Valor);
        Reserva.realCompr(Gas);
        double Por;
        Por = Reserva.porcentaje(Gas);        
        this.lblPoDi.setText(formatter.format(Por) + "%");
        
        java.util.List<ClsVentas> nuevo = new ArrayList<>();
        ClsVentas agregar = new ClsVentas();
        agregar.setCompra(Double.toString(Reserva.getCantidad()));
        agregar.setNombre(txtCliente.getText());
        agregar.setTipo(Gas);
        nuevo.add(agregar);
        Reserva.Actualizar(nuevo);
        JOptionPane.showMessageDialog(this, "Datos actualizados! \nRevisar archivo.csv");
        
        this.poblarData();
    }//GEN-LAST:event_btnDieselMouseClicked

