    private void poblarData(){
        DefaultTableModel model = (DefaultTableModel)this.tblTodo.getModel();
        while(model.getRowCount()>0){
            model.removeRow(0);//limpia
        }
         
        ClsReserva obje = new ClsReserva();
        for(ClsVentas temp : obje.getData())
        {
            String text = String.format("%s", temp.getCompra());
            Object[] arre = new Object[3];
            arre[0] = text;
            arre[1] = temp.getTipo();
            arre[2] = temp.getNombre();
            model.addRow(arre);
        }
    }

